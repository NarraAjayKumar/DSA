public class Boilerplate01 {
    public static void main(String args[]) {
        System.out.print("Hello,World!"); //We want to print "Hello,World!" without a newline at the end.
        System.out.println("Hello,World!");//We want to print "Hello,World!" with a newline at the end.
        System.out.printf("Hello,World!"); //We want to print "Hello,World
        System.out.print("Hello,World!\n"); //We want to print "Hello,World!" with a newline at the end using \n.
    }
}